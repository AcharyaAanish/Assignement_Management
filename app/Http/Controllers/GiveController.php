    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $given = new Give();

        $given->subject_name = $request->input('subject_name');
        $given->topic = $request->input('topic');
        $given->given_time = $request->input('given_time');
        $given->submit_time = $request->input('submit_time');
        $given->file = $request->input('file');

        $given->save();

        return $given;
    }

